package model.elements;

public interface Moves {

	/**
	 *
	 * @param x
	 * @param y
	 */
	void moveUp(int x, int y);

	/**
	 *
	 * @param x
	 * @param y
	 */
	void moveDown(int x, int y);

	/**
	 *
	 * @param x
	 * @param y
	 */
	void moveLeft(int x, int y);

	/**
	 *
	 * @param x
	 * @param y
	 */
	void moveRight(int x, int y);

}
